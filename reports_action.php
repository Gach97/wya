<?php
session_start();
if (!isset($_SESSION['user_id'])) {
    header('Location: login.php');
    exit();
}
require_once 'database.php';

$action = $_POST['action'] ?? '';

if ($action === 'export') {
    $report_name = $_POST['report_name'] ?? '';
    $report_type = $_POST['report_type'] ?? '';
    $format = $_POST['format'] ?? 'pdf';
    $notes = $_POST['notes'] ?? '';

    if (empty($report_name) || empty($report_type)) {
        $_SESSION['error'] = 'Report name and type are required';
        header('Location: reports.php');
        exit();
    }

    // Gather data for the report
    $total_files = $conn->query("SELECT COUNT(*) as count FROM objects")->fetch_assoc()['count'];
    $total_size = $conn->query("SELECT SUM(file_size) as total FROM objects")->fetch_assoc()['total'];
    $scanned_files = $conn->query("SELECT COUNT(*) as count FROM objects WHERE is_scanned = TRUE")->fetch_assoc()['count'];
    $scan_coverage = $total_files > 0 ? round(($scanned_files / $total_files) * 100, 1) : 0;

    // Generate report content based on type
    $report_data = [
        'name' => $report_name,
        'type' => $report_type,
        'format' => $format,
        'generated_at' => date('Y-m-d H:i:s'),
        'generated_by' => $_SESSION['username'] ?? 'System',
        'notes' => $notes,
        'total_files' => $total_files,
        'total_size' => $total_size,
        'scanned_files' => $scanned_files,
        'scan_coverage' => $scan_coverage
    ];

    if ($format === 'csv') {
        // Generate CSV
        header('Content-Type: text/csv');
        header('Content-Disposition: attachment; filename="' . sanitizeFilename($report_name) . '.csv"');
        
        $output = fopen('php://output', 'w');
        
        // Write headers
        fputcsv($output, ['Report Name', 'Type', 'Generated', 'Generated By']);
        fputcsv($output, [$report_name, $report_type, date('Y-m-d H:i:s'), $_SESSION['username'] ?? 'System']);
        fputcsv($output, []);
        
        if ($report_type === 'executive_summary' || $report_type === '') {
            fputcsv($output, ['Executive Summary']);
            fputcsv($output, ['Metric', 'Value']);
            fputcsv($output, ['Total Files', $total_files]);
            fputcsv($output, ['Total Size (MB)', round($total_size / 1048576, 2)]);
            fputcsv($output, ['Scanned Files', $scanned_files]);
            fputcsv($output, ['Scan Coverage %', $scan_coverage]);
        } elseif ($report_type === 'access_activity') {
            $result = $conn->query("SELECT COUNT(*) as total_actions, COUNT(DISTINCT user_id) as unique_users FROM audit_logs WHERE logged_at >= DATE_SUB(NOW(), INTERVAL 30 DAY)");
            $data = $result->fetch_assoc();
            fputcsv($output, ['Access Activity Report (Last 30 Days)']);
            fputcsv($output, ['Metric', 'Value']);
            fputcsv($output, ['Total Actions', $data['total_actions']]);
            fputcsv($output, ['Unique Users', $data['unique_users']]);
        } elseif ($report_type === 'storage') {
            $result = $conn->query("SELECT b.name, COUNT(o.id) as file_count, SUM(o.file_size) as total_size FROM buckets b LEFT JOIN objects o ON b.id = o.bucket_id GROUP BY b.id");
            fputcsv($output, ['Storage Distribution by Bucket']);
            fputcsv($output, ['Bucket Name', 'File Count', 'Total Size (MB)']);
            while ($row = $result->fetch_assoc()) {
                fputcsv($output, [$row['name'], $row['file_count'], round($row['total_size'] / 1048576, 2)]);
            }
        } elseif ($report_type === 'file_types') {
            $result = $conn->query("SELECT mime_type, COUNT(*) as count FROM objects GROUP BY mime_type ORDER BY count DESC");
            fputcsv($output, ['File Type Analysis']);
            fputcsv($output, ['MIME Type', 'Count']);
            while ($row = $result->fetch_assoc()) {
                fputcsv($output, [$row['mime_type'], $row['count']]);
            }
        } elseif ($report_type === 'user_activity') {
            $result = $conn->query("SELECT u.username, COUNT(al.id) as action_count FROM users u LEFT JOIN audit_logs al ON u.id = al.user_id WHERE al.logged_at >= DATE_SUB(NOW(), INTERVAL 30 DAY) GROUP BY u.id ORDER BY action_count DESC");
            fputcsv($output, ['User Activity Report (Last 30 Days)']);
            fputcsv($output, ['Username', 'Action Count']);
            while ($row = $result->fetch_assoc()) {
                fputcsv($output, [$row['username'], $row['action_count']]);
            }
        } elseif ($report_type === 'compliance') {
            fputcsv($output, ['Compliance Report']);
            fputcsv($output, ['Control', 'Status']);
            fputcsv($output, ['Data Encryption', 'COMPLIANT']);
            fputcsv($output, ['Access Control', 'COMPLIANT']);
            fputcsv($output, ['Audit Logging', 'COMPLIANT']);
            fputcsv($output, ['Data Classification', 'COMPLIANT']);
        }
        
        fclose($output);
        exit();

    } elseif ($format === 'json') {
