            $report_data['data'] = $result->fetch_assoc();
        } elseif ($report_type === 'storage') {
            $result = $conn->query("SELECT b.name, COUNT(o.id) as file_count, SUM(o.file_size) as total_size FROM buckets b LEFT JOIN objects o ON b.id = o.bucket_id GROUP BY b.id");
            $report_data['data'] = $result->fetch_all(MYSQLI_ASSOC);
        } elseif ($report_type === 'file_types') {
            $result = $conn->query("SELECT mime_type, COUNT(*) as count FROM objects GROUP BY mime_type ORDER BY count DESC");
            $report_data['data'] = $result->fetch_all(MYSQLI_ASSOC);
        } elseif ($report_type === 'user_activity') {
            $result = $conn->query("SELECT u.username, COUNT(al.id) as action_count FROM users u LEFT JOIN audit_logs al ON u.id = al.user_id WHERE al.logged_at >= DATE_SUB(NOW(), INTERVAL 30 DAY) GROUP BY u.id ORDER BY action_count DESC");
            $report_data['data'] = $result->fetch_all(MYSQLI_ASSOC);
        } elseif ($report_type === 'compliance') {
            $report_data['data'] = [
                'controls' => [
                    ['name' => 'Data Encryption', 'status' => 'COMPLIANT'],
                    ['name' => 'Access Control', 'status' => 'COMPLIANT'],
                    ['name' => 'Audit Logging', 'status' => 'COMPLIANT'],
                    ['name' => 'Data Classification', 'status' => 'COMPLIANT']
                ]
            ];
        }
        
        echo json_encode($report_data, JSON_PRETTY_PRINT | JSON_UNESCAPED_SLASHES);
        exit();

    } else {
        // Generate HTML/PDF as downloadable file
        try {
            $html = generatePDFContent($report_type, $report_name, $notes);
            
            // Send as HTML file (can be printed to PDF from browser)
            header('Content-Type: text/html; charset=utf-8');
            header('Content-Disposition: attachment; filename="' . sanitizeFilename($report_name) . '.html"');
            echo $html;
            exit();
        } catch (Exception $e) {
            $_SESSION['error'] = 'Error generating report: ' . $e->getMessage();
            header('Location: reports.php');
            exit();
        }
    }
}

function sanitizeFilename($filename) {
    $filename = preg_replace('/[^a-zA-Z0-9_\-]/', '_', $filename);
    return substr($filename, 0, 200);
}

